a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

print("Sum:", a + b)
print("Difference:", a - b)
print("Product:", a * b)
print("Quotient:", a / b)
print("Floor Division:", a // b) 
print("Modulus:", a % b)
print("Exponentiation:", a ** b)
